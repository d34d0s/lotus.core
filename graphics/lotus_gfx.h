typedef enum lotus_buffer_attr {
    LOTUS_LOCATION_ATTR = 1 << 0, // 1 (0b0001)
    LOTUS_COLOR_ATTR    = 1 << 1, // 2 (0b0010)
    LOTUS_TCOORD_ATTR   = 1 << 2, // 4 (0b0100)
    LOTUS_NORMAL_ATTR   = 1 << 3, // 8 (0b1000)
    SO_VERTEX_ATTRIBS  = 1 << 4 
} lotus_buffer_attr;

typedef struct lotus_buffer {
    ubyte4 handle[3];
    ubyte attrs;
    lotus_memory mem;
} lotus_buffer;

typedef struct lotus_pipeline {
    void* shader;
    void* sampler;
    struct bindings {
        void* vertex_buffer;
        void* element_buffer;
    } bindings;
} lotus_pipeline;

